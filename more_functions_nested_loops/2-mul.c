/**
 * mul - Multiplies two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Description: This function takes two integers as input parameters
 * and returns their product as an integer.
 *
 * Return: The product of the integers a and b
 */
int mul(int a, int b);
{
return (a * b);
}
